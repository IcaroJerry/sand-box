    private void setTableData() {
        IUser[] users;
        
        if(ServerController.getInstance().getUsersConnected() == null || 
           ServerController.getInstance().getUsersConnected().size() == 0)
            return;
        
        users = new IUser[ServerController.getInstance().getUsersConnected().size()];
        for(int i = 0; i < users.length; ++i) {
            users[i] = ServerController.getInstance().getUsersConnected().get(i);
        }
        
        if (serverTableModel == null) {
            serverTableModel = new UserTableModel();
            tblListUsers.setModel(serverTableModel);
        }
        tblListUsers.getSelectionModel().removeListSelectionListener(listSelectionListener);
        serverTableModel.setUsers(users);
        tblListUsers.getSelectionModel().addListSelectionListener(listSelectionListener);
    }
    
